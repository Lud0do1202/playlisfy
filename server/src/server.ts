app.get("/", async (req, res) => {
    res.send({ message: "Hello Word!" });
});

/* -------------------------------------------------------------------------- */
/*                                   CYANITE                                  */
/* -------------------------------------------------------------------------- */
/* ---------------------------------- Login --------------------------------- */
